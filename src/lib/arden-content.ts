export function formatCurrency(value: number, compact = false) {
  if (compact && Math.abs(value) >= 1_000_000) {
    return `£${(value / 1_000_000).toFixed(2)}M`;
  }
  return new Intl.NumberFormat("en-GB", {
    style: "currency",
    currency: "GBP",
    maximumFractionDigits: 0,
  }).format(value);
}
